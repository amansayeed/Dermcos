<p>Fungal infections occur when an invading fungus takes over an area of the skin, scalp or nail making their home in these moist areas of the body : between the toes, in the genital area, nails, ear and under the breasts. Common fungal skin infections are caused by yeasts (such as Candida or Malassezia furfur) or dermatophytes, such as Epidermophyton, Microsporum, and caused Trichophyton and can be a common culprit of itchy skin.
 Fungal infections are Common and the contributory factors are seasonal migration, international travel, extreme weather, excessive sweating, obesity, uncontrolled diabetes, weakened immune system, unhygienic practices and use of immunomodulatory agents for treating various diseases like cancer. 
The symptoms of fungal skin infections may vary attributing to the causative fungi and site of infection include 
</p>

<li><span>Rash on the skin, often with raised edges
</span></li>
<li>Redness, blisters

</li>
<li>Itching, stinging, soreness or burning sensations 
 
</li>
<li>Peeling skin
 
</li>
<li>Cracking skin 
</li>
<li>Pain on the infected body part (ear, foot. nail, scalp. etc.)
 
</li>
<li>Breaking and loss of infected hair 
</li>
<li>Crumbling and discoloration of finger and toe nail 
 
</li>
<br>
<p>The specialists usually diagnose a fungal Skin infection by looking at a red, irritated, or scaly rash in one of the commonly affected areas. A confirmatory test is done by scraping off a small amount of skin, a fragment of nail or hair and having it examined under a microscope  or placed in a culture medium where the specific fungus can grow and be identified.
 Most cases of fungal skin infections can be easily treated if dealt early with antifungal drugs; usually these antifungal drugs are applied directly to the affected area. These Topical drugs may include creams, gels, lotions, solutions, or shampoos. Oral Antifungal drugs may also be taken depending on the severity of the infection. In some infections, corticosteroids are used in initial phase to relieve inflammation and itching. The duration of treatment may vary from two to eight weeks to up to a year and a half for nail fungal infections
</p>


