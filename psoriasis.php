<p>Psoriasis is not just a “skin condition.” It is a chronic (long-lasting) non communicable disease caused an overactive immune system which starts underneath the skin, as a common skin condition that speeds up life cycle of the skin cells. It causes cells to build up rapidly on the surface of the skin. The extra skin sells form scales and red patches that are itchy and can cause pain, burning sting sensation in some patients. Psoriasis has also been linked to other conditions like Psoriatic Arthritis, Type 2 diabetes, Cardiovascular disease, High blood pressure, Crohn’s Disease, High cholesterol, Depression & Ulcerative colitis i.e. patients with psoriasis have a greater likelihood of being diagnosed with the above mentioned conditions. Psoriasis is characterized by its severity which is measured by the physical and emotional impact of the disease on the patient’s life. The cause of various factors, including genetics, environmental factors, and the immune system. There are multiple types of psoriasis and each type of psoriasis has very distinct symptoms and characteristics that can appear on the skin in a variety of ways. The typically affected areas are outer elbow, knees or scalp, though it can appear on any location. Irrespective of the area and its appearance the patient needs to discuss it with his dermatologist the issues pertaining to his condition and the line of treatment.
</p>

<li><b>Treatment options</b></li>
<p>It’s important to know the different treatment options and keep trying until you find the right regimen. The line of treatment can be a monotherapy or a combination therapy depending upon the severity of the condition. Topical treatments, such as Topical corticosteroids, Vitamin D analogues. Anthralin, Topical Retinoids, Calcineurin inhibitors, Salicylic acid. Coal tar and Moisturizers are used for mild to moderate psoriasis, when the disease is more severe, creams are likely to be combined with light therapy like Sunlight. UVB phototherapy, Narrow band UVB phototherapy, Goeckerman therapy, Psoralen plus ultraviolet A (PUVA) and Excimer laser or oral medications like Retinoids, methotrexate and, Cyclosporine. Sometimes systemic medications, including biologic drugs are prescribed if psoriasis is significantly impacting the quality of life.
</p>


<li><b>Lifestyle Remedies
</b></li>

<b>Take daily baths</b>-<p>using lukewarm water and mild soaps that have added oils and fats
</p>
<b>Use moisturizer </b>-<p>After bathing, apply a heavy ointment based moisturizer while your skin is moist. During cold, dry weather, you may need to apply a moisturizer several times a day

</p>
<b>Expose your skin to small amounts of sunlight </b>-<p>A controlled amount of sunlight cam improve psoriasis.

</p>
<b>Avoid possible psoriasis triggers </b>-<p>Look for triggers, if any, worsens your psoriasis and take step to prevent or avoid them. Infection, Injuries to your skin, stress, smoking, alcohol and intense sun exposure can all worsen psoriasis.

</p>
<b>Diet & Exercise </b>-<p>– Eating right is beneficial for all. But if you’re living with psoriasis, making healthy choices is even more vital. Exercise & relaxation is important. Techniques like yoga, meditation, and deep-breathing exercise have proven to lower stress.
Treating psoriasis is critical to good disease management and overall health. In conjunction with your doctor find a treatment plan that minimize or eliminate your symptoms and enhances your quality of life.
</p>



   




