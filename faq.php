       		                        <h3 class="pink"> Psoriasis</h3>
                        <ul>
            <li> Aside from heredity, what can cause psoriasis?</li>
            </ul>
            
           <p>
	Currently known triggers of this condition include endocrine factors (pregnancy, calcium and vitamin D levels), psychogenic stress, trauma (medically termed &ldquo;Koebnerization&rdquo;), infections (in particular strep throat, also HIV), and certain medications.&nbsp;</p>
            
        </div>
                <div class="row paddingbottom">
        		
       		            <ul>
            <li> What associations with psoriasis should I be worried about?</li>
            </ul>
            
           <p>
	Psoriasis is associated with several significant conditions that can affect not just quality of life, but overall life expectancy. Psoriasis-associated arthritis is debilitating and the most common comorbidity, and is different from osteoarthritis (&ldquo;wear-and-tear&rdquo; type). Cardiovascular disease is the most prominent association with psoriasis, due to systemic inflammation. It can lead to heart attacks amongst other problems. Liver disease is also prevalent in psoriasis patients, so controlling your condition is very important.</p>
            
