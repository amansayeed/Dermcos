&quot;Arial&quot;,&quot;sans-serif&quot;;color:black">There are a whole host of therapeutic options for your psoriasis, depending on location, symptoms and severity. Basic techniques include moisturization and a healthy lifestyle (diet, exercise, stress reduction, etc). Topical therapies (steroids, vitamin D analogues, retinoids, anthralin, coal tar, calcineurin inhibitors, &beta;-hydroxy acids, etc) light-based options (Narrowband-UVB phototherapy or PUVA) are exteral modalities, while oral medications (Methotrexate, Cyclosporine, Soriatane, Apremilast and Tofacitinib) and injectables (Enbrel, Humira, Remicade, Stelara and several upcoming options in clinical trials) are available systemic options.&nbsp;<o:p></o:p></span></p>
            
