			      <p><strong id="aa">g. Sun Damage</strong></p>
			  <p>The sun has a significant and profound effect on the skin. Some changes caused due to sun exposure are as follows:</p>
			
			  <p><b>Premature Skin Aging:</b> The exposure to Ultraviolet rays of the sun also called UV exposure accounts for most premature skin ageing.
			  </p>
			   <p><b>Other Skin Changes:</b> Exposure to UV rays can lead to two common types of skin changes. The first one being thick skin in coarse wrinkles, especially on the back of the neck (cutis Rhomboidalis nuchae). The other one can be thining of the skin causing fine wrinkles, tearing of the skin as well as bruising. Other skin changes due to UV exposure can be seen as tiny blood vessels appearing on the skin, particularly covering the facial area. Changes in skin colour are also very common. UV exposure can lead to pigment changes visible as brown spots (solar lentigos), small white spots(guttate hypomelanosis), freckles as well as red areas on the side of the neck (poikiloderma of Civatte). Moles are also commonly visible in areas exposed to the sun.
			  </p>
