			<p><b>Cysts</b></p>
<p>A cyst can be explained as a cluster of cells which have grouped together to form a sac. The cells which form the shell of this sac are very distinctly abnormal (in behaviour and appearance) as compared to the cells in the surrounding location. They may contain air, fluids or even semi-solid material. Cysts usually vary in size- ranging from microscopic to the size of a ball whereas large cysts can displace internal organs.</p>
<p>Cysts occur due to genetic conditions, tumours, defective cells, chronic inflammatory conditions, infections, blockage of ducts causing fluid build-ups, breaking of vessels etc. Most cysts are benign caused due to plugged ducts or natural body outlets for secretions. However, some cysts may be tumours and are potentially malignant.
</p>
<p>Symptoms of cysts vary according to the type of cyst. Mostly, the patient will become aware of an abnormal lump. Cysts in the mammary gland may be painful, cysts in the brain cause headaches and internal cysts may not have any obvious symptoms and may only be visible through an MRI, CAT scan or Ultrasound.</p>
<b>A cyst can be treated through the following methods:</b>
<p><b>Draining:</b>Draining the cyst using a needle is done if a cyst is infected or ruptured. The fluids or other matter in the cyst can be removed using this method.</p>
<p><b>Anti-inflammatory medications:</b>Using certain anti-inflammatory medications such as Cortisone injections can reduce the inflammation in a cyst.</p>
<p><b>Surgery:</b>Surgical removal of the cyst is usually used when draining has no effect on the Cyst. Internal and hard to reach cysts are also removed using surgery.</p>

