			<strong id="aa">b. Common Skin Growths</strong>
           <p>As our skin progresses through our life, it has a tendency to accumulate different types of
spots. Many of these spots are commonplace and easily recognized during your evaluation.
The following is a list of some of these common skin lesions.</p>
<strong>Skin Tags</strong>
		   <p>A Skin Tag or an Acrochordon is a small benign tumour which is found commonly in areas where the skin forms creases (neck, armpit and groin). On the face, they usually occur on the eyelids. The size of a skin tag is usually that of a grain of rice. Skin tags usually occur due to skin rubbing up against the skin and are therefore found in skin creases and folds. Usually harmless, they are most often removed for cosmetic or aesthetic reasons. Treatment usually involves the following procedures:
</p>
		    <p><b>Cauterization - </b>in this process the skin tag is removed by burning it with the help of electrolysis.</p>
			 <p><b>Cryosurgery - </b>in this process the removal of the skin tag is done by freezing off the tag with the help of a probe containing liquid nitrogen.</p>
			 <p><b>Ligation - </b>The blood supply to the skin tag is interrupted.</p>
			 <p><b>Excision - </b>In excision, the skin tag is removed by cutting it off with a scalpel. </p>
			 <p>These procedures need to be performed by skin care professionals.</p>

			<p><b>Blood Vessel Related Lesions</b></p>
           <p>Broken blood vessels can be seen as red veins present on the chin, cheeks or the nose. Causal factors may include childbirth, pregnancy, use of birth control pills, UV exposure etc. The most common and effective treatment for blood vessel lesions is Laser treatment. In Laser treatment, the visible blood vessels are removed by light absorbed by the blood that destroys the vessel. The vessel then disappears restoring the skin’s natural appearance. Laser treatments are very safe and produce gradual results. While large veins may require multiple treatments, smaller ones may only require a single laser treatment. This form of treatment is safe and has very few side effects. However, temporary discolouration lasting five to fourteen days may be caused due to laser treatment. Blistering, crusting etc. of the affected skin may also occur, but usually disappears in a few days. Sunscreen must be used on the treated area to hasten the healing process.</p>
