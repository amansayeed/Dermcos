			  <p><strong id="aa">h. Rosacea</strong></p>

			  <p>Rosacea refers to the chronic inflammatory of skin condition that normally appears as the blushing or appearance of flushing of the cheeks and nose, that can spread to the chin, forehead, chest, neck and back and the ears. Rosacea can grow up into persistent erythema (redness), cystic nodules, papules, and pustules. More severe cases can advance into rhinophyma, where the oil glands present in the skin become swelled that makes the nose larger and the cheeks puffier. Some patients never experience papules, pustules or cystic nodules and only face the complexities of persistent erythema and telangiectasias (tiny blood vessels). Only half of the patients face eye troubles due to rosacea termed as "ocular rosacea". This can result in dryness in the eye and burning and feeling of having something in your eyes.
</p>

			  <p>Rosacea can occur in any age group including children, however, a fair-skinned adult between the ages of 30 and 50 are more prone to this condition. Patients suffering rosacea need to carefully choose their skin care products as wrong products can make your condition worst. Treatment regimens are planned as per the individual condition that helps in limiting the progression of this condition. Right education on how to avoid contact with all is also important.
</p>

