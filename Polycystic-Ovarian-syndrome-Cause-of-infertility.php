					            <h3>Causes</h3> 
								<p>In PCOS, many small, fluid-filled cyst like sacs grow inside the ovaries hence giving it a “polycystic” appearance. These sacs are follicles containing an immature egg. But these eggs do not attain enough maturity to trigger ovulation. The lack of ovulation imbalances the levels of female hormones such as  estrogen, progesterone, FSH, and LH. Eventually, the ratio of female hormones to male hormones gets disrupted leading to lower than usual estrogen and progesterone levels, while higher than usual androgen levels. This hormone imbalance causes skipping of menstrual periods and also creates problems in conception. It can contribute to long-term health problems like diabetes and heart disease. Contraceptive pills and diabetes drugs can help to fix the hormone imbalance and improve the symptoms.</p>
	                               <p> The exact causes of PCOS are still not known to doctors. It is believed that high levels of androgens prevent the ovaries from producing hormones and making eggs normally. Genes, insulin resistance, and inflammation have also been linked to the excess of androgen production.
</p>				
					            <h3>Symptoms of PCOS</h3>
								<p>Some women start seeing symptoms around the time of their first menstrual period. Others realize they have PCOS after they have gained a lot of weight or they had trouble getting pregnant.
